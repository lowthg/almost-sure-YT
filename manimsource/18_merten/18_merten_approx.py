def pdf_v2(weights, x_values, N=2**20, t_max=3000.0):
    dt = t_max / N
    t = np.arange(N, dtype=np.float64) * dt
    phi = np.ones(N)
    for w in weights:
        phi *= j0(w * t)
    phi[0] = 1.0
    rfft_phi = np.fft.rfft(phi)
    pdf_grid = (dt / np.pi) * np.real(rfft_phi)
    n_out = N // 2 + 1
    dx = 2.0 * np.pi / (N * dt)
    x_grid = np.arange(n_out) * dx
    x_full = np.concatenate([-x_grid[::-1], x_grid[1:]])
    pdf_full = np.concatenate([pdf_grid[::-1], pdf_grid[1:]])
    # NO clipping
    interp = interp1d(x_full, pdf_full, kind='linear', bounds_error=False, fill_value=0.0)
    return interp(x_values)

