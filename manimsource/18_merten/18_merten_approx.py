# ── pre-computed M(x) data (true values from hyperbola DP) ──────────────────
# We'll compute this on-the-fly for the plot points using a simple sieve
def build_mertens_data(x_max=10**10, n_pts=800):
    """Build arrays of (x, M(x)) for plotting, using sieve for small x."""
    # For up to 10^8 use direct sieve; above that use floor-quotient values
    import math

    u = min(x_max, 10**8)

    # Sieve of mu up to u
    mu = [0] * (u + 1)
    mu[1] = 1
    primes = []
    is_composite = bytearray(u + 1)
    for i in range(2, u + 1):
        if not is_composite[i]:
            primes.append(i)
            mu[i] = -1
        for p in primes:
            if i * p > u:
                break
            is_composite[i * p] = 1
            if i % p == 0:
                mu[i * p] = 0
                break
            else:
                mu[i * p] = -mu[i]

    # Cumulative sum = M
    M_arr = [0] * (u + 1)
    for i in range(1, u + 1):
        M_arr[i] = M_arr[i - 1] + mu[i]

    # Sample log-spaced points up to u
    xs_small = np.unique(np.round(np.logspace(0, np.log10(u), n_pts * 4 // 5)).astype(int))
    xs_small = xs_small[xs_small >= 1]
    Ms_small = np.array([M_arr[int(x)] for x in xs_small], dtype=float)

    if x_max <= u:
        return xs_small.astype(float), Ms_small

    # For x > u: need hyperbola DP (simplified — use floor-quotients of x_max)
    # We'll pre-compute a handful of key points via the explicit formula (approximate)
    # and supplement with the main DP results imported from mertens_large.py
    # For the Manim animation, use only sieve range (up to 1e8) + known values
    # Known M values at powers of 10:
    known_large = {
        10**9: -222,
        2*10**9: 1037,    # approximate placeholder
        5*10**9: -5000,   # placeholder
        10**10: -33722,
    }
    # Just use sieve for the animation up to 1e8, which is visually rich
    return xs_small.astype(float), Ms_small


def M_approx_vectorised(xs, n_zeros=20):
    """Vectorised evaluation of the zeta-zero approximation formula."""
    logxs = np.log(xs)
    sqxs  = np.sqrt(xs)
    result = np.full(len(xs), -2.0)
    nf = -1.

    for n in range(1, 40):
        # nf *= -n / (4*PI*PI)
        k = 2 * n
        nf *= -k * (k-1) / (4*PI*PI)
        zeta0 = mp.zeta(2*n+1)
        zeta = float(zeta0.real)
        term = np.pow(xs, -k) / (n * zeta * nf)
        result += term
        # print(term[0])


    # for i, z in enumerate(ZEROS_DATA[:n_zeros]):
    for i in range(n_zeros):
        rho = mp.zetazero(i+1)
        gamma = float(rho.imag)
        zetad = mp.zeta(rho, derivative=1)
        coeff = 1. / (rho * zetad)
        # print(i, gamma, 'coeff:', coeff)
        # gamma = z['rho_im']
        # cr    = z['coeff_re']
        # ci    = z['coeff_im']
        # x^(1/2 + i*gamma) = sqrt(x) * exp(i*gamma*log(x))
        cr = float(coeff.real)
        ci = float(coeff.imag)
        cos_g = np.cos(gamma * logxs)
        sin_g = np.sin(gamma * logxs)
        # Re[ coeff * x^rho ] = sqx * (cr*cos - ci*sin)
        result += 2.0 * sqxs * (cr * cos_g - ci * sin_g)

    return result


