        u = int(xs_plot.max())
        mu = np.ones(u + 1, dtype=np.int8); mu[0] = 0
        sq = int(u**0.5) + 1
        isp = np.ones(sq + 1, dtype=bool); isp[0] = isp[1] = False
        for p in range(2, int(sq**0.5) + 1):
            if isp[p]: isp[p*p::p] = False
        small_p = np.where(isp)[0].tolist()
        for p in small_p:
            mu[p::p] *= -1
            p2 = p * p
            if p2 <= u: mu[p2::p2] = 0
        cof = np.arange(u + 1, dtype=np.int32)
        for p in small_p: cof[p::p] //= p
        mu[(cof != 1) & (mu != 0)] *= -1
        M_sieve = np.cumsum(mu[1:]).astype(float)
        M_sieve = np.concatenate([[0.0], M_sieve])
